    public String getGrade(double score){
        String result;
        
        if(score >= 9){
            result = "Xuất sắc";
        } else if(score >= 8){
            result = "Giỏi";
        } else if(score >= 6.5){
            result = "Khá";
        } else if(score >= 5){
            result = "Trung bình";
        } else{
            result = "Yếu";
        }
        
        return result;
    }
    
    public void loadTable(){
        model.setRowCount(0);
        Object rowData[] = new Object[5];
        for(int i = 0; i < stl.stl.size(); i++){
            rowData[0] = stl.stl.get(i).getName();
            rowData[1] = stl.stl.get(i).getScore();
            rowData[2] = stl.stl.get(i).getMajor();
            rowData[3] = stl.stl.get(i).getGrade();
            if(stl.stl.get(i).isGift()){
                rowData[4] = "Có";
            } else{
                rowData[4] = "Không";
            }
            model.addRow(rowData);
        }
    }
        
    public void add(Student st){
        stl.them(st);
    }
    
