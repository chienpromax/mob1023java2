    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(txtMark.getText().length() == 0 || Double.parseDouble(txtMark.getText()) > 10.0 || Double.parseDouble(txtMark.getText()) < 0.0){
            JOptionPane.showMessageDialog(this, "Điểm không hợp lệ!", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else{
            Student st = new Student();
            
            st.setName(txtName.getText());
            st.setScore(Double.parseDouble(txtMark.getText()));
            st.setMajor(cbMajor.getSelectedItem().toString());
            st.setGrade(getGrade(Double.parseDouble(txtMark.getText())));
            if(chkGift.isSelected()){
                st.setGift(true);
            } else{
                st.setGift(false);
            }
            
            add(st);
            loadTable();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void txtMarkKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMarkKeyTyped
        // TODO add your handling code here:
        txtMark.addKeyListener(new KeyAdapter() {});
        char char_input = evt.getKeyChar();
        if (((char_input < '0') || (char_input > '9')) && (char_input != '\b') && (char_input != '.'))
        {
            JOptionPane.showMessageDialog(this, "Chỉ được nhập số!", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
            txtMark.setText("");
            evt.consume();
        }
    }//GEN-LAST:event_txtMarkKeyTyped

    private void txtMarkKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMarkKeyReleased
        // TODO add your handling code here:
        if(txtMark.getText().length() != 0){
            txtGrade.setText(getGrade(Double.parseDouble(txtMark.getText())));
        }
    }//GEN-LAST:event_txtMarkKeyReleased

    private void tblStudentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentsMouseClicked
        // TODO add your handling code here:
        int r = tblStudents.getSelectedRow();
        txtName.setText(tblStudents.getValueAt(r, 0).toString());
        txtMark.setText(tblStudents.getValueAt(r, 1).toString());
        cbMajor.setSelectedItem(tblStudents.getValueAt(r, 2).toString());
        txtGrade.setText(tblStudents.getValueAt(r, 3).toString());
        if(tblStudents.getValueAt(r, 4).toString().equalsIgnoreCase("Có")){
            chkGift.setSelected(true);
        } else{
            chkGift.setSelected(false);
        }
    }//GEN-LAST:event_tblStudentsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(txtMark.getText().length() != 0 && tblStudents.getSelectedRow() != -1){
            int r = tblStudents.getSelectedRow();
            stl.stl.get(r).setName(txtName.getText());
            stl.stl.get(r).setScore(Double.parseDouble(txtMark.getText()));
            stl.stl.get(r).setMajor(cbMajor.getSelectedItem().toString());
            stl.stl.get(r).setGrade(txtGrade.getText());
            if(chkGift.isSelected()){
                stl.stl.get(r).setGift(true);
            } else{
                stl.stl.get(r).setGift(false);
            }
            
            loadTable();
        } else{
            JOptionPane.showMessageDialog(this, "Điểm không hợp lệ hoặc chưa chọn dòng cần sửa!", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }    
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        if(stl.check()){
            JOptionPane.showMessageDialog(this, "Danh sách rỗng!", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else{
            if(tblStudents.getSelectedRow() != -1){
                int r = tblStudents.getSelectedRow();
                stl.stl.remove(r);
                loadTable();
            } else{
                JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xóa!", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
            }    
        } 
    }//GEN-LAST:event_btnRemoveActionPerformed

