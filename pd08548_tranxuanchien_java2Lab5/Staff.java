    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(list.isEmpty()){
            try {
                // TODO add your handling code here:
                list = (List<StaffClass>) XFile.readObject("staff.dat");
                this.fillToTable();
            } catch (IOException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Chưa có nhân viên!", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
            }
        } else{
            int dialogButton = 0;
            int dialogResult = JOptionPane.showConfirmDialog (null, "Bạn có muốn Load lại danh sách đã lưu?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                ((DefaultTableModel)tblEmp.getModel()).setNumRows(0);
                try {
                    // TODO add your handling code here:
                    list = (List<StaffClass>) XFile.readObject("staff.dat");
                    this.fillToTable();
                } catch (IOException | ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "Chưa có nhân viên!", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

