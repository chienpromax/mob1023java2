    private void addStaff(){
        StaffClass emp = new StaffClass();
        emp.fullName = txtName.getText();
        try{
            emp.Salary = Double.parseDouble(txtSalary.getText());
            list.add(emp);
            txtSalary.setBackground(Color.WHITE);
            JOptionPane.showMessageDialog(this, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }catch(NumberFormatException e){
            txtSalary.setBackground(Color.yellow);
            JOptionPane.showMessageDialog(this, "Lương không hợp lệ", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void fillToTable(){
        DefaultTableModel model = (DefaultTableModel) tblEmp.getModel();
        model.setRowCount(0);
        list.stream().map((nv) -> new Object[]{nv.fullName, String.format("%,.1f", nv.Salary)}).forEachOrdered((row) -> {
            model.addRow(row);
        });
    }

