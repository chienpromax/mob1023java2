    private void btnPreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreActionPerformed
        // TODO add your handling code here:
        saveAns();
        
        if(index > 0){
            index--;
        } else{
            index = 4;
        }
        
        loadQuestion();
        
        if(list.get(index).user == null){
            clear();
        }
    }//GEN-LAST:event_btnPreActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        saveAns();
        
        if(index < 4){
            index++;
        } else{
            index = 0;
        }
        
        loadQuestion();
        
        if(list.get(index).user == null){
            clear();
        }
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnFinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinActionPerformed
        // TODO add your handling code here:
        int score = 0;
        
        for(Sentences x : list){
            if(Objects.equals(x.getAnswer(), x.user)){
                    score += 20;
                    x.setQuestion(x.getQuestion() + " - Đúng");
                } else{
                    x.setQuestion(x.getQuestion() + " - Sai");
                }
            }
            
        JOptionPane.showMessageDialog(this, "Điểm số: " + score, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        reLoad();
    }//GEN-LAST:event_btnFinActionPerformed

