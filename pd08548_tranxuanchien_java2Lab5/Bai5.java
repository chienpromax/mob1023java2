    public void loadList(){
        String[] x = {"1", "2", "3", "4"};
        list.add(new Sentences("Kết quả: 1 + 1 = ?", x, 1));
        list.add(new Sentences("Kết quả: 2 x 1 = ?", x, 1));
        list.add(new Sentences("Kết quả: 1 x 1 = ?", x, 0));
        list.add(new Sentences("Kết quả: 3 + 1 = ?", x, 3));
        list.add(new Sentences("Kết quả: 4 - 1 = ?", x, 2));
    }
    
    public void loadQuestion(){
        lblQuestion.setText(list.get(index).getQuestion());
        ans1.setText(list.get(index).getChoices()[0]);
        ans2.setText(list.get(index).getChoices()[1]);
        ans3.setText(list.get(index).getChoices()[2]);
        ans4.setText(list.get(index).getChoices()[3]);
        lblNo.setText("Câu hỏi: " + (index + 1));
        if(list.get(index).user != null){
            if(list.get(index).user == 0){
                ans1.setSelected(true);
            }
            
            if(list.get(index).user == 1){
                ans2.setSelected(true);
            }
            
            if(list.get(index).user == 2){
                ans3.setSelected(true);
            }
            
            if(list.get(index).user == 3){
                ans4.setSelected(true);
            }
        }
    }
    
    public void reLoad(){
        lblQuestion.setText(list.get(index).getQuestion());
    }
    
    public void saveAns(){
        if(ans1.isSelected()){
            list.get(index).user = 0;
        }
        
        if(ans2.isSelected()){
            list.get(index).user = 1;
        }
        
        if(ans3.isSelected()){
            list.get(index).user = 2;
        }
        
        if(ans4.isSelected()){
            list.get(index).user = 3;
        }
    }
    
    public void clear(){
        buttonGroup1.clearSelection();
    }

